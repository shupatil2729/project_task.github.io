package String;

public class Replace {
    public static void main(String[] args) {
        String s="Shubham";
        System.out.println(s.replace("Shubham","patil"));
        System.out.println(s.replace("S","S_"));
        System.out.println("========================================");
        String p="PUNE CITY";
        System.out.println(p.replace("E","T"));
        System.out.println(p.replace("PUNE","MUMBAI"));
    }
}
