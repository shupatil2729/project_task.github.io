package Casting;

public class CastingDemo1 {
    public static void main(String[] args) {
        int a=10;
        double b=32.25;
        System.out.println("a "+a);
        System.out.println("b "+b);

        //narrowing
        int x=(int)35.25;

        double y=80;
        System.out.println("x "+x);
        System.out.println("y "+y);




    }
}
